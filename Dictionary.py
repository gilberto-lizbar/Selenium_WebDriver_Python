# Dictionary is an unordered sequence of data of key value pair form.
# It is similar to hashmap in java
dic = {"Age": 37, 1: "Topic", "flag": True, "Name": "Gilberto"}
print(dic)
# Dictionary allows to find a value per key name
print(dic["Age"])
print(dic[1])
print(dic["flag"])
# To declare an empty dictionary and add values
newDic = {}
newDic["Name"] = "Gilberto"
newDic["Last Name"] = "Lizarraga"
newDic["Age"] = 37
newDic[1] = "Position"
print(newDic)

